   void listado() {
        String sql = "select * from compra";
        try {
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);
            Object[] producto = new Object[5];
//            String[] Titulos={"ID","DNI","NOMBRES"};         
//            model=new DefaultTableModel(null,Titulos);   
            model = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                producto[0] = rs.getInt("id");
                producto[1] = rs.getString("factura_id_fk");
                producto[2] = rs.getInt("producto_id_fk");
                producto[3] = rs.getInt("cantidad");
                producto[4] = rs.getInt("precio");
                
                model.addRow(producto);
            }
            TablaDatos.setModel(model);

        } catch (Exception e) {
        }

    }
     void Agregar() {
        String fact = txtFactura.getText();
        int producto = Integer.parseInt(txtproducto.getText());
        int cantidad = Cantidad_unidad.getSelectedIndex()+1;
        int Precio =Integer.parseInt(txtTotal.getText());
        
        try {
            if (fact.equals("")) {
                JOptionPane.showMessageDialog(null, "Debe Ingresar Datos");
                limpiarTabla(model);
                           
            } else {
                String sql = "insert into compra (factura_id_fk, producto_id_fk,cantidad,precio) values('" + fact + "','" + producto + "','" +cantidad+ "','" + Precio + "')";
                System.out.println(sql);
                con = cn.getConnection();
                st = con.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "compra agregada");
                limpiarTabla(model);
          
                
            }

        } catch (Exception e) {
            System.out.println(e);
        }
     }
         void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i <= TablaDatos.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }

    }
